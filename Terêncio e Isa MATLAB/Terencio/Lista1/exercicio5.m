%Pedir um número entre 1 e 9 e escrevê-lo por extenso.
%E como preencher cheque, dado o valor?
%Dica: EX = ['UM   '; 'DOIS '; 'TRES '; ... ; 'NOVE '];

op = input("Digite um número entre 1 e 9: ");

switch(op ~= 0)
   case 2
    disp("DOIS")
   case 2
    disp("DOIS")
   case 3
    disp("TRES")
   case 4
    disp("QUATRO")
   case 5
    disp("CINCO")
   case 6
    disp("SEIS")
   case 7
    disp("SETE")
   case 8
    disp("OITO")
   case 9
    disp("NOVE")
end
